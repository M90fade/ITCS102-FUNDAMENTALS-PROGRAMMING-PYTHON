#create a countdown timer simulator 
#simulate a simple countdown timer using for loop that counts from a user-specified number down to 1, 
#then prints "Liftoff!"


print("COUNTDOWN TIMER SIMULATOR")

cd = eval(input("Enter the starting number for countdown:" ))
print("\nCountdown Started:")

for fern in range(cd, 0, -1):
    print(fern)
print("Liftoff!")